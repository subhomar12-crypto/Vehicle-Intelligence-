class DisclaimerType(Enum):
    """Types of disclaimers."""
    GENERAL = "general"                    # Always shown
    PREDICTION = "prediction"              # Shown with predictions
    CRITICAL_FAILURE = "critical_failure"  # For safety-critical predictions
    LOW_CONFIDENCE = "low_confidence"      # For low confidence predictions
    UNSUPPORTED_VEHICLE = "unsupported_vehicle"  # For unsupported vehicles
    EXPERIMENTAL = "experimental"          # For experimental features
    LEGAL = "legal"                        # Legal disclaimer
    PROFESSIONAL_ADVICE = "professional"   # Professional inspection advice


class DisclaimerSeverity(Enum):
    """Severity level of disclaimer."""
    CRITICAL = "critical"    # Must be acknowledged, blocks action
    WARNING = "warning"      # Should be acknowledged
    INFO = "info"            # Informational
    LEGAL = "legal"          # Legal requirement


