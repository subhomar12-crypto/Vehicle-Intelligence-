@dataclass
class Disclaimer:
    """A disclaimer to be displayed to the user."""
    disclaimer_id: str
    disclaimer_type: DisclaimerType
    severity: DisclaimerSeverity
    title: str
    text: str
    must_acknowledge: bool
    context: Dict[str, Any] = field(default_factory=dict)
    created_at: str = field(default_factory=lambda: datetime.now().isoformat())


@dataclass
class DisclaimerAcknowledgment:
    """Record of user acknowledging a disclaimer."""
    acknowledgment_id: str
    disclaimer_id: str
    disclaimer_type: str
    user_id: str
    acknowledged_at: str
    ip_address: Optional[str] = None
    device_info: Optional[str] = None


# =============================================================================
# SAFETY DISCLAIMER MANAGER
# =============================================================================

class SafetyDisclaimerManager:
    """
    Manages safety disclaimers throughout the application.

    CRITICAL: This system ensures all predictions are accompanied by
    appropriate disclaimers. Disabling or bypassing disclaimers is
    a safety violation.
    """

    def __init__(self, storage_path: Optional[Path] = None):
        """Initialize disclaimer manager."""
        self.storage_path = storage_path or Path("ai_data/disclaimers")
        self.storage_path.mkdir(parents=True, exist_ok=True)

        self.templates = DISCLAIMER_TEMPLATES
        self._acknowledgments: Dict[str, DisclaimerAcknowledgment] = {}
        self._session_shown: set = set()

        self._load_acknowledgments()

        logger.info("Safety Disclaimer Manager initialized")

    def _load_acknowledgments(self):
        """Load previous acknowledgments."""
        ack_file = self.storage_path / "acknowledgments.json"
        if ack_file.exists():
            try:
                with open(ack_file, 'r') as f:
                    data = json.load(f)
                    for ack_data in data.get('acknowledgments', []):
                        ack = DisclaimerAcknowledgment(**ack_data)
                        key = f"{ack.user_id}:{ack.disclaimer_type}"
                        self._acknowledgments[key] = ack
            except Exception as e:
                logger.warning(f"Failed to load acknowledgments: {e}")

    def _save_acknowledgments(self):
        """Save acknowledgments to file."""
        ack_file = self.storage_path / "acknowledgments.json"
        try:
            data = {
                'acknowledgments': [
                    {
                        'acknowledgment_id': a.acknowledgment_id,
                        'disclaimer_id': a.disclaimer_id,
                        'disclaimer_type': a.disclaimer_type,
                        'user_id': a.user_id,
                        'acknowledged_at': a.acknowledged_at,
                        'ip_address': a.ip_address,
                        'device_info': a.device_info
                    }
                    for a in self._acknowledgments.values()
                ]
            }
            with open(ack_file, 'w') as f:
                json.dump(data, f, indent=2)
        except Exception as e:
            logger.warning(f"Failed to save acknowledgments: {e}")

    def get_disclaimer(
        self,
        disclaimer_type: DisclaimerType,
        context: Optional[Dict[str, Any]] = None
    ) -> Disclaimer:
        """
        Get a disclaimer of the specified type.

        Args:
            disclaimer_type: Type of disclaimer to get
            context: Context variables for template substitution

        Returns:
            Disclaimer object
        """
        template = self.templates.get(disclaimer_type, self.templates[DisclaimerType.GENERAL])

        # Format text with context
        text = template['text']
        if context:
            try:
                text = text.format(**context)
            except KeyError as e:
                logger.warning(f"Missing context variable for disclaimer: {e}")

        disclaimer_id = hashlib.sha256(
            f"{disclaimer_type.value}:{datetime.now().isoformat()}".encode()
        ).hexdigest()[:16]

        return Disclaimer(
            disclaimer_id=disclaimer_id,
            disclaimer_type=disclaimer_type,
            severity=template['severity'],
            title=template['title'],
            text=text.strip(),
            must_acknowledge=template['must_acknowledge'],
            context=context or {}
        )

    def get_disclaimers_for_prediction(
        self,
        prediction: Dict[str, Any],
        vehicle_coverage: Optional[Dict[str, Any]] = None
    ) -> List[Disclaimer]:
        """
        Get all relevant disclaimers for a prediction.

        Args:
            prediction: The prediction data
            vehicle_coverage: Vehicle coverage information

        Returns:
            List of Disclaimer objects that must be shown
        """
        disclaimers = []

        # Always include general prediction disclaimer
        disclaimers.append(self.get_disclaimer(DisclaimerType.PREDICTION))

        # Check for critical failure
        failure_type = prediction.get('failure_type', '')
        criticality = prediction.get('criticality', '')

        if criticality == 'critical' or failure_type in ['fuel_pump', 'coolant_system', 'transmission']:
            disclaimers.append(self.get_disclaimer(
                DisclaimerType.CRITICAL_FAILURE,
                {'failure_type': failure_type.replace('_', ' ').title()}
            ))

        # Check confidence level
        confidence = prediction.get('confidence', 1.0)
        if confidence < 0.5:
            disclaimers.append(self.get_disclaimer(
                DisclaimerType.LOW_CONFIDENCE,
                {'confidence': round(confidence * 100, 1)}
            ))

        # Check vehicle coverage
        if vehicle_coverage:
            coverage_level = vehicle_coverage.get('coverage_level', '')
            if coverage_level in ['unsupported', 'minimal']:
                disclaimers.append(self.get_disclaimer(
                    DisclaimerType.UNSUPPORTED_VEHICLE,
                    {
                        'year': vehicle_coverage.get('year', 'Unknown'),
                        'make': vehicle_coverage.get('make', 'Unknown'),
                        'model': vehicle_coverage.get('model', 'Unknown')
                    }
                ))

        # Add professional advice if probability is high
        failure_prob = prediction.get('failure_probability', 0)
        if failure_prob > 0.5:
            disclaimers.append(self.get_disclaimer(
                DisclaimerType.PROFESSIONAL_ADVICE,
                {
                    'prediction_summary': f"{failure_type} failure predicted",
                    'confidence': round(confidence * 100, 1)
                }
            ))

        return disclaimers

    def get_legal_disclaimer(self) -> Disclaimer:
        """Get the legal disclaimer (required on first use)."""
        return self.get_disclaimer(DisclaimerType.LEGAL)

    def check_acknowledgment_required(
        self,
        disclaimer: Disclaimer,
        user_id: str
    ) -> bool:
        """
        Check if user needs to acknowledge this disclaimer.

        Args:
            disclaimer: The disclaimer to check
            user_id: User identifier

        Returns:
            True if acknowledgment is required
        """
        if not disclaimer.must_acknowledge:
            return False

        key = f"{user_id}:{disclaimer.disclaimer_type.value}"

        # Check if already acknowledged
        if key in self._acknowledgments:
            return False

        # Check session-based showing
        session_key = f"session:{disclaimer.disclaimer_type.value}"
        template = self.templates.get(disclaimer.disclaimer_type)

        if template:
            frequency = template.get('show_frequency', 'always')

            if frequency == 'once_per_session' and session_key in self._session_shown:
                return False

        return True

    def record_acknowledgment(
        self,
        disclaimer: Disclaimer,
        user_id: str,
        ip_address: Optional[str] = None,
        device_info: Optional[str] = None
    ) -> DisclaimerAcknowledgment:
        """
        Record user acknowledgment of a disclaimer.

        Args:
            disclaimer: The disclaimer being acknowledged
            user_id: User identifier
            ip_address: IP address for audit
            device_info: Device info for audit

        Returns:
            Acknowledgment record
        """
        ack_id = hashlib.sha256(
            f"{disclaimer.disclaimer_id}:{user_id}:{datetime.now().isoformat()}".encode()
        ).hexdigest()[:16]

        acknowledgment = DisclaimerAcknowledgment(
            acknowledgment_id=ack_id,
            disclaimer_id=disclaimer.disclaimer_id,
            disclaimer_type=disclaimer.disclaimer_type.value,
            user_id=user_id,
            acknowledged_at=datetime.now().isoformat(),
            ip_address=ip_address,
            device_info=device_info
        )

        key = f"{user_id}:{disclaimer.disclaimer_type.value}"
        self._acknowledgments[key] = acknowledgment

        # Track in session
        session_key = f"session:{disclaimer.disclaimer_type.value}"
        self._session_shown.add(session_key)

        self._save_acknowledgments()

        logger.info(f"Disclaimer {disclaimer.disclaimer_type.value} acknowledged by {user_id}")

        return acknowledgment

    def has_acknowledged_legal(self, user_id: str) -> bool:
        """Check if user has acknowledged legal disclaimer."""
        key = f"{user_id}:{DisclaimerType.LEGAL.value}"
        return key in self._acknowledgments

    def format_disclaimer_for_display(
        self,
        disclaimer: Disclaimer,
        format_type: str = 'text'
    ) -> str:
        """
        Format disclaimer for display.

        Args:
            disclaimer: Disclaimer to format
            format_type: 'text', 'html', or 'markdown'

        Returns:
            Formatted disclaimer string
        """
        if format_type == 'html':
            severity_class = disclaimer.severity.value
            return f"""
<div class="disclaimer disclaimer-{severity_class}">
    <h3 class="disclaimer-title">{disclaimer.title}</h3>
    <div class="disclaimer-text">{disclaimer.text.replace(chr(10), '<br>')}</div>
    {f'<div class="disclaimer-acknowledge">Please acknowledge to continue</div>' if disclaimer.must_acknowledge else ''}
</div>
"""
        elif format_type == 'markdown':
            severity_icon = {
                DisclaimerSeverity.CRITICAL: "🚨",
                DisclaimerSeverity.WARNING: "⚠️",
                DisclaimerSeverity.INFO: "ℹ️",
                DisclaimerSeverity.LEGAL: "⚖️"
            }.get(disclaimer.severity, "")

            return f"""
{severity_icon} **{disclaimer.title}**

{disclaimer.text}

{"---\n*Acknowledgment required to continue*" if disclaimer.must_acknowledge else ""}
"""
        else:
            # Plain text
            border = "=" * 60
            return f"""
{border}
{disclaimer.title.upper()}
{border}

{disclaimer.text}

{"[ACKNOWLEDGMENT REQUIRED]" if disclaimer.must_acknowledge else ""}
{border}
"""

    def get_mandatory_disclaimers_text(self) -> str:
        """Get concatenated text of all mandatory disclaimers."""
        mandatory_types = [
            DisclaimerType.GENERAL,
            DisclaimerType.LEGAL
        ]

        texts = []
        for dtype in mandatory_types:
            disclaimer = self.get_disclaimer(dtype)
            texts.append(self.format_disclaimer_for_display(disclaimer, 'text'))

        return "\n\n".join(texts)


# =============================================================================
# SINGLETON ACCESS
# =============================================================================

_manager_instance: Optional[SafetyDisclaimerManager] = None


def get_disclaimer_manager() -> SafetyDisclaimerManager:
    """Get or create singleton manager instance."""
    global _manager_instance
    if _manager_instance is None:
        _manager_instance = SafetyDisclaimerManager()
    return _manager_instance


def get_prediction_disclaimers(
    prediction: Dict[str, Any],
    vehicle_coverage: Optional[Dict[str, Any]] = None
) -> List[Disclaimer]:
    """Convenience function to get disclaimers for a prediction."""
    manager = get_disclaimer_manager()
    return manager.get_disclaimers_for_prediction(prediction, vehicle_coverage)


def format_disclaimer(disclaimer: Disclaimer, format_type: str = 'text') -> str:
    """Convenience function to format a disclaimer."""
    manager = get_disclaimer_manager()
    return manager.format_disclaimer_for_display(disclaimer, format_type)
